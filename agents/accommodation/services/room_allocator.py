"""
=====================================================================
인원 기반 검색 조건 (기능명세서 p0 - 첫 번째 항목)
=====================================================================

[이 파일이 하는 일 한 줄 요약]
    "성인 3명, 아동 2명(4세, 7세)" 같은 여행 인원 정보를 받아서,
    호텔 API(LiteAPI)에 검색을 요청할 때 필요한 "방 배정안"으로 바꿔주는 파일.

[왜 이게 필요한가?]
    호텔 예약은 "인원 몇 명"이 아니라 "방을 몇 개, 각 방에 누가 묵을지"로 검색해야 해.
    예를 들어 4인 가족이 여행가도 호텔 방 하나에 4명이 다 못 들어가는 경우가 많아서
    (방마다 최대 수용인원이 정해져 있음) 여러 방으로 쪼개야 하거든.
    이 로직이 없으면 사용자가 "4명이요"라고 말했을 때 우리가 알아서
    "그럼 2인실 2개로 나눠서 검색해드릴게요"라고 판단해줄 수가 없어.

[LiteAPI가 요구하는 최종 결과물 형태] (공식 문서 확인함)
    occupancies = [
        {"adults": 2, "children": [4, 7]},   <- 방 1: 성인 2명 + 4세,7세 아동
        {"adults": 1}                         <- 방 2: 성인 1명만
    ]
    즉, "방(room)"을 기준으로 한 리스트이고, 리스트의 길이 = 방 개수.
    각 원소(딕셔너리)가 방 하나를 의미하고, 그 방에 성인 몇 명 + 아동은 몇 살인지를 담음.

[이 파일의 핵심 함수]
    allocate_rooms(adults, children_ages)
        -> 인원수를 넣으면 방 배정안(RoomOccupancy 리스트)을 돌려줌

    to_liteapi_occupancies(rooms)
        -> 위 결과를 LiteAPI가 원하는 딕셔너리 형태로 최종 변환
=====================================================================
"""

# dataclass: 파이썬에서 "데이터를 담는 용도의 클래스"를 짧게 정의할 수 있게 해주는 기능.
#           class를 쓰고 __init__을 직접 안 써도, @dataclass 붙이면 자동으로 만들어줌.
from dataclasses import dataclass, field

# ceil: "올림" 함수. 예: ceil(5 / 2) = ceil(2.5) = 3.
#       사람은 소수점 단위로 나눌 수 없으니까, 나머지가 생기면 방을 하나 더 잡아야 함.
from math import ceil

# 타입 힌트용. List[int]처럼 "이 변수는 정수 리스트야"라고 표시해서
# 다른 사람(혹은 미래의 나)이 코드를 읽을 때 헷갈리지 않게 해줌.
from typing import List


# ---------------------------------------------------------------------------
# 정책 상수 (기본값)
# ---------------------------------------------------------------------------
# 왜 "상수"로 뺐나?
#   나중에 "우리 정책은 방당 성인 3명까지 허용하자" 같은 변경이 생기면
#   코드 여기저기 숫자를 찾아 바꾸는 게 아니라 이 값 하나만 바꾸면 되게 하려고.
#   (매직 넘버를 코드 안에 그냥 박아넣지 않는 습관!)

MAX_ADULTS_PER_ROOM = 2    # 방 하나에 최대 성인 몇 명까지 넣을지 (호텔 업계 표준 디폴트)
MAX_CHILDREN_PER_ROOM = 2  # 방 하나에 최대 아동 몇 명까지 넣을지


# ---------------------------------------------------------------------------
# 데이터 구조 정의
# ---------------------------------------------------------------------------

@dataclass
class RoomOccupancy:
    """
    방 1개의 배정 결과를 담는 자료구조.

    예를 들어 RoomOccupancy(adults=2, children=[4, 7]) 이라고 하면
    "이 방에는 성인 2명, 4살/7살 아동 2명이 묵는다"는 뜻.
    """
    adults: int
    # field(default_factory=list): "기본값을 빈 리스트로 하고 싶다"는 뜻.
    # 파이썬에서 dataclass 필드의 기본값으로 [](빈 리스트)를 바로 못 쓰게 되어있어서
    # (여러 인스턴스가 같은 리스트를 공유하는 버그를 막기 위한 파이썬 자체 규칙)
    # 이렇게 default_factory를 통해 "새 리스트를 매번 새로 만들어라"고 지정해줘야 함.
    children: List[int] = field(default_factory=list)

    def to_liteapi_dict(self) -> dict:
        """
        이 방 정보를 LiteAPI가 이해하는 딕셔너리 형태로 변환.
        예: RoomOccupancy(2, [4,7]) -> {"adults": 2, "children": [4, 7]}

        아동이 없는 방이면 "children" 키 자체를 안 넣음
        (LiteAPI 문서 예시들도 아동 없을 땐 children 키를 생략하는 패턴을 씀)
        """
        result = {"adults": self.adults}
        if self.children:  # 빈 리스트([])는 False로 취급되므로, 아동이 1명 이상 있을 때만 추가
            result["children"] = self.children
        return result


class RoomAllocationError(ValueError):
    """
    인원 조건이 물리적으로 말이 안 될 때 발생시키는 에러.
    예: 성인은 0명인데 아동만 있다 -> 아동은 보호자 없이 혼자 호텔에 못 묵음.

    ValueError를 상속받은 이유: "입력값이 잘못됐다"는 의미를 명확히 하려고.
    나중에 이 함수를 호출하는 쪽(예: Django View)에서
    `except RoomAllocationError`로 잡아서 "사용자에게 400 에러 응답 보내기" 같은
    처리를 할 수 있게 됨.
    """


# ---------------------------------------------------------------------------
# 핵심 함수: allocate_rooms
# ---------------------------------------------------------------------------

def allocate_rooms(
    adults: int,
    children_ages: List[int] | None = None,
    max_adults_per_room: int = MAX_ADULTS_PER_ROOM,
    max_children_per_room: int = MAX_CHILDREN_PER_ROOM,
) -> List[RoomOccupancy]:
    """
    인원수를 받아서 "방을 몇 개로, 어떻게 나눌지" 결정하는 메인 함수.

    ------------------------------------------------------------
    파라미터 (입력값) 설명
    ------------------------------------------------------------
    adults:
        성인 총 인원수. 1 이상이어야 함 (호텔 예약엔 성인이 최소 1명은 필요).
    children_ages:
        아동들의 나이를 담은 리스트. 예: [4, 7] = 4살 아동 1명 + 7살 아동 1명.
        아동이 없으면 None을 넘기거나 아예 생략 가능 (기본값 None).
    max_adults_per_room:
        방 하나에 넣을 수 있는 성인 최대 인원. 기본은 2명.
    max_children_per_room:
        방 하나에 넣을 수 있는 아동 최대 인원. 기본은 2명.

    ------------------------------------------------------------
    반환값
    ------------------------------------------------------------
    RoomOccupancy 객체들의 리스트. 리스트 길이 = 필요한 방 개수.

    ------------------------------------------------------------
    동작 원리를 예시로 이해하기
    ------------------------------------------------------------
    예시: 성인 4명 + 아동 5명(나이: 2,3,5,7,9), 방당 최대 성인2/아동2 정책

    1단계) 성인 기준 필요한 방 개수 계산
        4명 ÷ 2명(방당 최대) = 2개  (나누어 떨어짐)

    2단계) 아동 기준으로도 필요한 방 개수 계산
        5명 ÷ 2명(방당 최대) = 2.5 -> 올림(ceil) -> 3개

    3단계) 둘 중 더 큰 값 채택
        max(2, 3) = 3개 방 필요
        (성인만 보면 2개 방으로 충분해 보이지만, 아동 5명을 방당 2명씩
         나눠 넣으려면 최소 3개 방이 있어야 하기 때문)

    4단계) 성인 4명을 3개 방에 최대한 균등 분배
        4 ÷ 3 = 1.33... -> [2, 1, 1] 처럼 나눔 (앞쪽 방부터 1명씩 더 배정)

    5단계) 아동 5명을 "성인이 있는 방들"에 균등 분배
        [2, 2, 1] 처럼 나눔 (방당 2명 넘지 않게)

    최종 결과:
        방1: 성인 2, 아동 2명(나이: 2,3)
        방2: 성인 1, 아동 2명(나이: 5,7)
        방3: 성인 1, 아동 1명(나이: 9)
    ------------------------------------------------------------
    예외 상황
    ------------------------------------------------------------
    RoomAllocationError 발생 조건:
        - adults가 0 이하인데 아동만 있는 경우 (보호자 없이 아동 단독 배정 불가)
        - adults가 0 이하이고 아동도 없는 경우 (애초에 인원이 없음)
    """

    # children_ages가 None으로 들어오면 빈 리스트로 바꿔줌.
    # 이렇게 해두면 아래 코드에서 매번 "None인지 체크"를 안 해도 되고,
    # 그냥 "빈 리스트냐 아니냐"만 신경쓰면 되서 코드가 깔끔해짐.
    children_ages = children_ages or []

    # -----------------------------------------------------------
    # 입력값 검증: 말이 안 되는 조합이면 여기서 바로 에러를 던짐
    # -----------------------------------------------------------
    if adults <= 0:
        if children_ages:
            # 성인 0명 + 아동만 있는 경우 -> 절대 불가능한 조합
            raise RoomAllocationError(
                "성인 없이 아동만으로는 객실을 배정할 수 없습니다."
            )
        # 성인도 0명, 아동도 없음 -> 애초에 여행 인원이 없다는 뜻
        raise RoomAllocationError("성인 인원(adults)은 1명 이상이어야 합니다.")

    # -----------------------------------------------------------
    # 1단계: 성인 인원 기준으로 필요한 방 개수 계산
    # -----------------------------------------------------------
    # ceil(올림)을 쓰는 이유: 예를 들어 성인 5명, 방당 최대 2명이면
    #   5 / 2 = 2.5인데, 방을 2.5개 만들 수는 없으니 올려서 3개로 잡아야 함
    #   (방 2개면 4명밖에 못 들어가서 1명이 남기 때문)
    num_rooms = ceil(adults / max_adults_per_room)

    # -----------------------------------------------------------
    # 2단계: 아동이 있다면, "아동을 다 수용하려면 방이 몇 개 필요한지"도 계산해서
    #         성인 기준 방 개수와 비교 -> 더 큰 쪽을 최종 방 개수로 채택
    # -----------------------------------------------------------
    if children_ages:
        rooms_needed_for_children = ceil(len(children_ages) / max_children_per_room)
        num_rooms = max(num_rooms, rooms_needed_for_children)

    # -----------------------------------------------------------
    # 3단계: 성인을 방들에 최대한 균등하게 분배
    # -----------------------------------------------------------
    # _even_split이 뭘 하는 함수인지는 아래에서 따로 설명할게 (헬퍼 함수 섹션 참고)
    adult_counts = _even_split(total=adults, num_buckets=num_rooms)

    # RoomOccupancy 객체들을 미리 만들어둠 (이 시점엔 아동은 아직 안 넣은 상태)
    rooms = [RoomOccupancy(adults=count) for count in adult_counts]

    # -----------------------------------------------------------
    # 4단계: 혹시라도 "성인이 0명인 방"이 생겼다면 재조정
    # -----------------------------------------------------------
    # 언제 이런 일이 생기냐면: 예를 들어 성인 2명인데 아동이 많아서
    # (아동 기준으로) 방을 4개 잡아야 하는 상황이면,
    # 성인 2명을 4개 방에 나누면 방 2개는 성인이 0명이 되어버림.
    # 성인 0명인 방에는 아동을 넣을 수 없으니(보호자 없음), 이런 경우
    # 방 개수를 "성인이 실제로 채울 수 있는 만큼"으로 줄여야 함.
    if any(count == 0 for count in adult_counts):
        num_rooms = min(num_rooms, adults)  # 방 개수를 성인 수 이하로 제한
        adult_counts = _even_split(adults, num_rooms)  # 다시 균등 분배
        rooms = [RoomOccupancy(adults=count) for count in adult_counts]

    # -----------------------------------------------------------
    # 5단계: 아동을 "성인이 있는 방"들에 균등 분배
    # -----------------------------------------------------------
    if children_ages:
        _assign_children(rooms, children_ages, max_children_per_room)

    return rooms


# ---------------------------------------------------------------------------
# 헬퍼(보조) 함수들
# ---------------------------------------------------------------------------
# 헬퍼 함수로 따로 뺀 이유: allocate_rooms 함수 하나가 너무 길어지고 복잡해지는 걸
# 막기 위해서. "균등 분배"라는 하나의 작은 역할만 담당하는 함수를 따로 만들면
# 테스트하기도 쉽고, 나중에 "균등 분배 방식"만 바꾸고 싶을 때 여기만 수정하면 됨.

def _even_split(total: int, num_buckets: int) -> List[int]:
    """
    total(전체 인원)을 num_buckets(방 개수) 개로 최대한 균등하게 나눈
    정수 리스트를 반환.

    예시: _even_split(5, 3)
        5 // 3 = 1 (기본으로 각 방에 1명씩)
        5 % 3 = 2 (나머지 2명은 어딘가에 더 배정해야 함)
        -> 앞쪽 2개 방에 1명씩 더 줘서 [2, 2, 1] 반환
        (2+2+1 = 5, 총합이 total과 일치하는지 항상 확인하는 습관을 들이면 좋음)
    """
    base = total // num_buckets       # //는 "정수 나눗셈" (몫만 취함, 소수점 버림)
    remainder = total % num_buckets   # %는 "나머지"

    # 리스트 컴프리헨션: for문을 한 줄로 쓰는 파이썬 문법.
    # "i가 0부터 num_buckets-1까지 돌면서, i가 remainder보다 작으면 base+1, 아니면 base"
    # 라는 뜻. 즉 나머지만큼 앞쪽 방부터 1명씩 더 얹어주는 것.
    return [base + 1 if i < remainder else base for i in range(num_buckets)]


def _assign_children(
    rooms: List[RoomOccupancy],
    children_ages: List[int],
    max_children_per_room: int,
) -> None:
    """
    아동들을 "성인이 1명 이상 있는 방"들에 균등 분배해서
    각 RoomOccupancy 객체의 children 필드를 직접 채워넣는 함수.

    주의: 이 함수는 반환값이 없음(None). 대신 rooms 리스트 안의
    객체들을 "직접 수정(in-place)"하는 방식으로 동작함.
    (파이썬은 리스트/객체를 함수에 넘기면 "참조"가 전달되기 때문에
     함수 안에서 수정하면 함수 밖에서도 그 수정이 반영됨)
    """
    # rooms 중에서 성인이 1명 이상 있는 방만 골라냄
    # (성인이 0명인 방엔 절대 아동을 넣으면 안 되니까)
    eligible_rooms = [r for r in rooms if r.adults > 0]

    if not eligible_rooms:
        # 이론적으로는 allocate_rooms의 4단계에서 이미 걸러지긴 하지만,
        # 혹시 모를 상황을 대비한 방어 코드 (안전벨트 같은 거)
        raise RoomAllocationError("아동을 배정할 성인 방이 없습니다.")

    num_children = len(children_ages)
    # 아동 수를, 성인이 있는 방 개수만큼 균등 분배
    per_room_counts = _even_split(num_children, len(eligible_rooms))

    # 어느 한 방이라도 정책상 최대 아동 수(max_children_per_room)를 넘으면
    # 지금 이 방 개수로는 수용이 불가능하다는 뜻 -> 에러를 던져서
    # 상위 로직(예: 방 개수를 늘리는 재시도 로직)이 알아채게 함
    if max(per_room_counts) > max_children_per_room:
        raise RoomAllocationError(
            f"아동 {num_children}명을 방당 최대 {max_children_per_room}명 정책으로 "
            f"배정할 수 없습니다. 방 개수를 늘려야 합니다."
        )

    # 실제로 아동 나이 리스트를 방마다 순서대로 잘라서 배정
    idx = 0  # children_ages 리스트에서 지금 어디까지 배정했는지 가리키는 포인터
    for room, count in zip(eligible_rooms, per_room_counts):
        # 슬라이싱: children_ages[idx : idx+count] -> idx번째부터 count개를 잘라옴
        room.children = children_ages[idx: idx + count]
        idx += count  # 다음 방을 위해 포인터를 count만큼 이동


# ---------------------------------------------------------------------------
# LiteAPI 요청 포맷으로 최종 변환하는 함수
# ---------------------------------------------------------------------------

def to_liteapi_occupancies(rooms: List[RoomOccupancy]) -> List[dict]:
    """
    allocate_rooms()가 만든 RoomOccupancy 리스트를,
    LiteAPI에 실제로 보낼 수 있는 딕셔너리 리스트(occupancies)로 변환.

    사용 예:
        rooms = allocate_rooms(adults=2, children_ages=[4, 7])
        occupancies = to_liteapi_occupancies(rooms)
        # occupancies == [{"adults": 2, "children": [4, 7]}]
        # 이걸 그대로 hotel_search.py의 search_hotel_candidates()에
        # occupancies 파라미터로 넘기면 됨
    """
    return [room.to_liteapi_dict() for room in rooms]